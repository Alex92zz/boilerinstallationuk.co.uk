                            <h2 class="title">ANTI-BRIBERY AND CORRUPTION POLICY</h2>
                            <ol>
                                <h3>Statement and purpose of policy</h3>

                                <li>
                                    As involvement in bribery and corruption exposes MINT Commercial Ltd (the Business) and
                                    its employees and representatives to a criminal offence; damages the Business’
                                    reputation; and damages the confidence of any clients or customers, suppliers and
                                    business partners; the Business is committed to conducting its business in an honest and
                                    ethical manner. </li>
                                <li>
                                    Bribery and corruption are criminal offences in most countries where the Business
                                    conducts its business and operations. As a UK-registered company, the Business is
                                    subject to the Bribery Act 2010 (the Act).
                                </li>
                                <li>
                                    The Business has a zero-tolerance approach towards bribery and corruption anywhere in
                                    its business and is committed to:
                                </li>
                                <ol type="a">
                                    <li> acting in a professional and fair manner;</li>
                                    <li> acting with integrity in all its business dealings and relationships; and </li>
                                    <li> implementing and enforcing effective systems to counter bribery and corruption.
                                    </li>
                                </ol>


                                <h3>What does this policy cover?</h3>

                                <li> This policy covers bribery and corruption taking place anywhere in the Business (within
                                    the UK or abroad).</li>
                                <li> This policy sets out the steps everyone in the Business must take to prevent bribery
                                    and corruption within the Business, in accordance with the relevant legislation and the
                                    Business’ requirements.</li>
                                <li> This policy does not form part of any employment contract and the Business retains the
                                    right to amend it at any time, at its absolute discretion.</li>



                                <h3>What are bribery and corruption?</h3>

                                <li> A ‘bribe’ is any inducement or reward that is offered, promised, requested or provided
                                    in order to gain a commercial, contractual, regulatory or personal advantage. In most
                                    cases, a bribe will be a financial or other advantage given to a person in order for
                                    them to perform a relevant function or activity improperly, or to reward them for doing
                                    so.</li>
                                <ol type="a">
                                    <li>Bribes, in the form of financial or other advantages, may include:</li>
                                    <ol type="i">
                                        <li> money (whether in the form of cash or cash equivalent);</li>
                                        <li> gifts;</li>
                                        <li> hospitality and entertainment;</li>
                                        <li> loans;</li>
                                        <li> services;</li>
                                        <li> preferential treatment;</li>
                                        <li> discounts; and</li>
                                        <li> promises to provide financial or other advantages in the future.</li>
                                    </ol>
                                    <li>For something to be considered a bribe and be subject to this policy:</li>
                                    <ol type="i">
                                        <li> the timing of the bribe is irrelevant and any payments made, or advantages
                                            given, after a relevant event are considered bribes;</li>
                                        <li> the payment made, or advantage given, can be given or received unknowingly; and
                                        </li>
                                        <li> it is also not necessary for the bribed party to actually receive a benefit as
                                            a result of the bribe.</li>
                                    </ol>
                                </ol>

                                <li>Bribery’ includes:</li>
                                <ol type="a">
                                    <li> giving, offering or promising a bribe;</li>
                                    <li> requesting, receiving or agreeing to receive a bribe; or</li>
                                    <li> bribing a foreign public official (as defined in the Act).</li>
                                </ol>

                                <li>‘Corruption’ is the misuse of power or office for private gain.</li>
                                <li>This means that no one should:</li>
                                <ol type="a">
                                    <li> offer or provide a bribe (e.g. any payment, gift, hospitality or other benefit) to
                                        reward the business advantage received, or in the expectation that a business
                                        advantage will be received.</li>
                                    <li> accept a third party’s offer that they know or suspect to be made with the
                                        expectation that it will provide a business advantage (to the third party or anyone
                                        else).</li>
                                    <li> offer or provide a payment to a government official in any country (in the UK or
                                        abroad) to facilitate or speed up a necessary or routine procedure.</li>
                                    <li> fail to prevent bribery and corruption from occurring.</li>
                                </ol>

                                <li>No one must intimidate, threaten or retaliate against another person who has refused to
                                    accept or offer a bribe or who has raised concerns under this policy.</li>
                                <li> For the purposes of this policy, it does not matter whether: </li>
                                <ol type="a">
                                    <li> bribery and corruption occur in the UK or abroad. Any act of bribery or corruption
                                        committed outside of the UK may be prosecuted in the UK; or</li>
                                    <li> the act of bribery and corruption is committed directly or indirectly.</li>
                                </ol>

                                <h3>Who can be involved in bribery and corruption?</h3>

                                <li>Bribery and corruption can be committed by:</li>

                                <ol type="a">
                                    <li>a. any worker of the Business, irrespective of seniority, tenure and working hours,
                                        including all employees, directors and officers, consultants and contractors,
                                        temporary and agency workers, trainees, casual and fixed-term staff, apprentices,
                                        interns and any volunteers (Staff);</li>
                                    <li> anyone otherwise authorised to act on the behalf of Staff;</li>
                                    <li> the Business’ representatives and any other third parties who act on the Business’
                                        behalf;</li>
                                    <li> the Business’ suppliers; and</li>
                                    <li> the Business’ clients or customers (e.g. a customer may attempt to induce someone
                                        working for the Business to give that customer more favourable treatment).</li>
                                </ol>

                                <li> This policy and the rules contained within it apply to those listed in paragraph 13
                                    above.</li>

                                <h3>In what circumstances can bribery and corruption occur? </h3>

                                <li> Bribery and corruption can take place in the public and private sectors.</li>
                                <li> Typically, the person receiving the bribe can influence the progress of or be aware of
                                    relevant business due to their position. The person receiving the bribe will often, but
                                    not always, be a government or public official.</li>


                                <h3>Who is responsible for this policy?</h3>

                                <li> The Board of Directors has overall responsibility for this policy. </li>
                                <li> The Business Manager has been appointed as the person with primary and day-to-day
                                    operational responsibility for implementing this policy. They will also monitor the
                                    policy’s use and effectiveness and ensure that it is adhered to. </li>
                                <li> Management personnel at all levels are responsible for ensuring those reporting to them
                                    are made aware of and understand this policy and are given adequate and regular training
                                    on it.</li>


                                <h3>Gifts and hospitality</h3>

                                <li> All Staff are forbidden from soliciting any gifts or hospitality in the course of their
                                    work for the Business.</li>
                                <li> All Staff are forbidden from offering or receiving gifts or hospitality which are
                                    unduly lavish, extravagant or otherwise inappropriate from any person or organisation
                                    which has had, has or may have influence over the Business’ business. The following is a
                                    non-exhaustive list of gifts and hospitality the Business deems inappropriate:</li>
                                <ol type="a">
                                    <li> Hospitality valued at more than £20.</li>
                                    <li> Gifts, be they personal or corporate, with a value greater than £20.</li>
                                    <li> Any gifts that include cash or cash equivalents (including, but not limited to,
                                        vouchers).</li>
                                    <li> Any hospitality or gifts given or received in secret.</li>
                                    <li> Any hospitality or gifts received in the name of an individual rather than the
                                        Business’ name. </li>
                                </ol>
                                <p>If you have any questions about gifts and hospitality, contact the Business Manager at
                                    jamesgomes@hotmail.co.uk.</p>

                                <h3>Keeping records</h3>

                                <li> As transparency is crucial and false or misleading records could be damaging to the
                                    Business, it is essential that the Business keeps a full and accurate record of all
                                    financial dealings. Under relevant money laundering regulations, the Business’
                                    accountants and lawyers are required to report anything that seems irregular.</li>
                                <li> As a result, Staff must declare and properly record in writing all hospitality and
                                    gifts received or given. In relation to any hospitality, gifts or payments to third
                                    parties (including suppliers and customers), Staff must: </li>
                                <ol type="a">
                                    <li> submit expense claims in accordance with the Business’ Expense Policy available
                                        from the HR Department; and</li>
                                    <li> record in writing the reason for the expenditure.</li>
                                </ol>
                                <li> All accounts, invoices, purchase orders, credit notes and other records relating to
                                    third parties must be accurately and fully prepared in accordance with the Business’
                                    procedures, practices and requirements.</li>

                                <h3>Reporting issues related to bribery and corruption</h3>

                                <li> All Staff have a responsibility to comply with this policy and prevent bribery and
                                    corruption. Staff who:</li>
                                <ol type="a">
                                    <li> witness or otherwise discover anything corrupt or otherwise improper taking place;
                                    </li>
                                    <li> are offered a bribe;</li>
                                    <li> are asked to offer a bribe; or</li>
                                    <li> suspect or discover that any bribery or corruption has taken place or may take
                                        place;</li>
                                    <p>must notify their manager and the Legal Department as soon as possible. Staff can do
                                        this anonymously. As Staff must report issues related to bribery and corruption as
                                        soon as possible, any delays will need to be explained.</p>
                                </ol>

                                <h3>Consequences of non-compliance</h3>

                                <li> The Business takes compliance with this policy very seriously and failure to comply
                                    with this policy puts both Staff and the Business at significant risk.</li>
                                <li> Staff who fail to comply with this policy may commit a criminal offence and the
                                    criminal law relating to bribery and corruption carries several penalties.</li>
                                <li> Due to the importance of this policy, failure to comply with any of its procedures and
                                    requirements may result in disciplinary action and/or dismissal for gross misconduct.
                                    Any non-Staff who breach this policy may have their contract terminated with immediate
                                    effect.</li>
                                <li> If you have any questions or concerns about anything in this policy, please contact the
                                    Business Manager at jamesgomes@hotmail.co.uk.</li>

